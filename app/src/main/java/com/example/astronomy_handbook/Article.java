package com.example.astronomy_handbook;

public class Article {

    private String mArticle;
    private int mArticleImage;

    public Article(String article, int articleImage) {
        mArticle = article;
        mArticleImage = articleImage;
    }

    public String getmArticle() {
        return mArticle;
    }

    public int getmArticleImage() {
        return mArticleImage;
    }

}
